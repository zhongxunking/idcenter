package org.antframework.ids.facade.enums;

/**
 * 周期类型
 */
public enum PeriodType {
    /**
     * 每小时算一个周期
     */
    HOUR,

    /**
     * 每一天算一个周期
     */
    DAY,

    /**
     * 每一月算一个周期
     */
    MONTH,

    /**
     * 每一年算一个周期
     */
    YEAR,

    /**
     * 无周期
     */
    NONE,
}
